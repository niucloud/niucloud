SET NAMES 'utf8';
DROP TABLE IF EXISTS nc_file;
CREATE TABLE nc_file (
  id INT(11) NOT NULL AUTO_INCREMENT,
  site_id INT(11) NOT NULL DEFAULT 0 COMMENT '站点id',
  category_id INT(11) NOT NULL DEFAULT 0 COMMENT '文件分组',
  file_name VARCHAR(255) NOT NULL DEFAULT '' COMMENT '上传文件名',
  path VARCHAR(255) NOT NULL DEFAULT '' COMMENT '实际文件名以及路径',
  file_ext VARCHAR(255) NOT NULL DEFAULT '' COMMENT '文件扩展名',
  size VARCHAR(255) NOT NULL DEFAULT '' COMMENT '文件大小',
  type VARCHAR(255) NOT NULL DEFAULT 'IMAGE' COMMENT '类型（IMAGE,AUDIO,VIDEO）',
  pic_spec VARCHAR(255) NOT NULL DEFAULT '' COMMENT '规格（适用图片）',
  big_pic_path VARCHAR(255) NOT NULL DEFAULT '' COMMENT '大图路径',
  big_pic_spec VARCHAR(255) NOT NULL DEFAULT '' COMMENT '大图规格',
  mid_pic_path VARCHAR(255) NOT NULL DEFAULT '' COMMENT '中图路径',
  mid_pic_spec VARCHAR(255) NOT NULL DEFAULT '' COMMENT '中图规格',
  small_pic_path VARCHAR(255) NOT NULL DEFAULT '' COMMENT '小图路径',
  small_pic_spec VARCHAR(255) NOT NULL DEFAULT '' COMMENT '小图规格',
  create_time INT(11) NOT NULL DEFAULT 0 COMMENT '创建时间',
  modify_time INT(11) NOT NULL DEFAULT 0 COMMENT '修改时间',
  remark VARCHAR(1000) NOT NULL DEFAULT '' COMMENT '备注',
  PRIMARY KEY (id)
)
ENGINE = INNODB
AUTO_INCREMENT = 1
CHARACTER SET utf8
COLLATE utf8_general_ci
COMMENT = '文件管理';

DROP TABLE IF EXISTS nc_file_category;
CREATE TABLE nc_file_category (
  id int(11) NOT NULL AUTO_INCREMENT,
  site_id int(11) NOT NULL DEFAULT 0 COMMENT '站点id',
  name varchar(255) NOT NULL DEFAULT '' COMMENT '分组名称',
  type varchar(255) NOT NULL DEFAULT 'IMAGE' COMMENT '类型（IMAGE,AUDIO,VIDEO）',
  is_default int(11) NOT NULL DEFAULT 0 COMMENT '是否为默认相册,1代表默认',
  sort int(11) NOT NULL DEFAULT 0 COMMENT '排序',
  cover varchar(255) NOT NULL DEFAULT '' COMMENT '封面',
  PRIMARY KEY (id)
)
ENGINE = INNODB
AUTO_INCREMENT = 1
CHARACTER SET utf8
COLLATE utf8_general_ci
COMMENT = '文件分组';